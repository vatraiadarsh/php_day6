DRINKER	VARCHAR(30)	NOT NULL,
	CONSTRAINT DRINKERS_PKEY PRIMARY KEY (DRINKER));

CREATE TABLE LOCATED(	/* Pubs have locations */
PUB			VARCHAR(30)	NOT NULL,	/* Pub name	*/
STREET		VARCHAR(30)	NOT NULL,	/* Street name	*/
