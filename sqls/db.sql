CREATE TABLE SERVES(	/* Pubs serve drinks */
PUB			VARCHAR(30)	NOT NULL,	/* Pub name	*/
DRINK		VARCHAR(30)	NOT NULL,	/* Drink name	*/
PRICE		DECIMAL(5,2)	NOT NULL,	/* Drink F	*/
