		REFERENCES ALLDRINKS(DRINK)  );

CREATE TABLE LIKES(	/* Drinkers like drinks	*/
DRINKER		VARCHAR(30)	NOT NULL,	/* Drinker name	*/
DRINK		VARCHAR(30)	NOT NULL,	/* Drink name	*/
RATING		DECIMAL(1)	NOT NULL,	/* Rating of the drink	*/
